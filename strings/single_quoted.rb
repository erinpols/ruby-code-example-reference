# Single quoted strings are the simplest string in Ruby
# Only escape sequences recognized are (\') and (\\), everything else is treated literally
puts 'This is a string' # prints: This is a string
# Escaped single quote
puts 'Connan O\'Brian' # prints: Connan O'Brian - the apostrophe is IN the string literal
# Escaped backslash
puts 'The foler is in C:\\HOME' # prints: The folder is in C:\HOME